    private void BtnChangePassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnChangePassActionPerformed
    if(!validateInput())
    {
        return;
    }
    try{
    boolean result=UserDao.changePassword(jcReceptionistId.getSelectedItem().toString(),txtPassword.getPassword().toString());
    if(!result)
    {
    JOptionPane.showMessageDialog(null, "Record Unchanged","Error",JOptionPane.INFORMATION_MESSAGE);
    
    }
    else
        JOptionPane.showMessageDialog(null, "Pasword Changed","Success",JOptionPane.INFORMATION_MESSAGE);
    }
    catch(Exception e){
    e.printStackTrace();
    }
    
    
    }//GEN-LAST:event_BtnChangePassActionPerformed

