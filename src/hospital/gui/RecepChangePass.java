    public void loadReceptionistList(){
    try{
    receptionistList=UserDao.getReceptionistList();
    if(receptionistList.size()==0){
    JOptionPane.showMessageDialog(null,"No Registerd present");
    return;
    }
    Set<String> receptionistIds=receptionistList.keySet();
    for(String id:receptionistIds)
       jcReceptionistId.addItem(id);
    }
    catch(SQLException e){
        e.printStackTrace();}
    
    }
    
   private boolean validateInput(){
       char[]pwd=txtPassword.getPassword();
       char[]repwd=txtRePassword.getPassword();
       if(pwd.length==0||repwd.length==0)
       {JOptionPane.showMessageDialog(null, "Please enter both Pass","Error",JOptionPane.INFORMATION_MESSAGE);
       return false;
    }
       password=String.valueOf(pwd);
       String rePassword=String.valueOf(repwd);
       if(!password.equals(rePassword))
       {
       JOptionPane.showMessageDialog(null, "Password Donot Match","Error",JOptionPane.INFORMATION_MESSAGE);
        return  false;
       }
       return true;
          
    }

